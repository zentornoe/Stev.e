// If Reset BT Pushed
void reset_state(){
  
  // motor control (Stop)
  TankStop();
  HAL_Delay(10);

  // Default value (Middle Value) : motor speed, solar tracker, gimbal direction
  S_dir=4;
  G_dir=4;
  
  SolarTracker();
  HAL_Delay(10);
  GimbalCtrl();
  HAL_Delay(10);
}


void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
  if(htim->Instance==TIM3)      // Timer 3 -> Timer Interrupt : every 5[s]
    SolarChk(&hadc1);           // Check the CDS
    SolarTracker();             // Move Solar Tracker
    BatteryChk(&hadc2);         // Check Battery Level
    printf("G%d", G_dir+1);
}




